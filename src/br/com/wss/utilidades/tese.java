    public void tetse(JDateChooser date) {
        LocalDate hoje = LocalDate.now();
        DateTimeFormatter formatador = DateTimeFormatter.ofPattern("yyyyMMdd");
        String data = hoje.format(formatador);
        
        System.out.println("data atual "+data);
        String test = ClassUtils.getDateChooser(date).replace("-","");
        System.out.println(test);
        
        int dias = Integer.parseInt(test)-Integer.parseInt(data);
        System.out.println("Dias "+dias);

    }

